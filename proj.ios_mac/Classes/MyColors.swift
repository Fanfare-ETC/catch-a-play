import Foundation
import UIKit

extension UIColor {
    static func primary() -> UIColor {
        return UIColor(red: 1.0, green: 1.0, blue: 0.76, alpha: 1.0);
    }
    
    static func primaryDark() -> UIColor {
        return UIColor(red: 0.50, green: 0.38, blue: 0.0, alpha: 1.0);
    }
    
    static func tertiary() -> UIColor {
        return UIColor(red: 0.77, green: 0.21, blue: 0.15, alpha: 1.0);
    }
    
    static func tertiaryDark() -> UIColor {
        return UIColor(red: 0.75, green: 0.21, blue: 0.15, alpha: 1.0);
    }
    
    static func accent() -> UIColor {
        return UIColor(red: 0.56, green: 0.73, blue: 0.76, alpha: 1.0);
    }
    
    static func secondary() -> UIColor {
        return UIColor(red: 0.0, green: 0.17, blue: 0.40, alpha: 1.0);
    }
    
    static func green() -> UIColor {
        return UIColor(red: 0.0, green: 0.50, blue: 0.0, alpha: 1.0);
    }
}
